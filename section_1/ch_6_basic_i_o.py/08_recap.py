"""
Recap - True or False


Challenge

Write a program that gets an input from the user, a number, 1 or 0.

The program will output "T" if the input is 1 and "F" otherwise.

Remember! you can cast the input to number using int()
"""

num = input()
if num == "0":
    print("F")
elif num == "1":
    print("T")
